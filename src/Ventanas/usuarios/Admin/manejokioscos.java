     public void iniciarkiosko(){
         
         llenarRegion();
         kiosko();
         
     }
     
     public void llenarRegion(){
         Set<String>nombresRegion = new HashSet<>();
        for(int a = 0; a<Logica.tamañoR();a++){
            String nombre = Logica.imprimirR(a).getNombreregion();
            if(!nombresRegion.contains(nombre)){
                origenR.addElement(nombre);
                nombresRegion.add(nombre);
            }            
        }
     }
     
     public void kiosko(){
         
         String[] columnas = {"Codigo", "Nombre kiosko", "Region"};
         DefaultTableModel modelo = new DefaultTableModel(columnas,0);
         
         for(int i = 0; i<tamaño();i++){
             
             String kiosko = "";
             
             Object[] fila =  
             {completa(i).getCodigo(),
             completa(i).getNombre(),
             completa(i).getCodigoRegion()
             };
             modelo.addRow(fila);
         }
         tablakioscos.setModel(modelo);
         
     }
     
     public int tamaño(){
        return kioscos.size();
     }
     public boolean insertar(Kiosco m){
         int a = 0;
         
         for(int x = 0;x<tamaño();x++){
             
             if(kioscos.get(x).getCodigo().equalsIgnoreCase(m.getCodigo())){
                 a=1;
             }
         }
         if(a==0){
             kioscos.add(m);
             return true;
         }else{
             return false;
         }
         
     }
     public Kiosco completa (int a){
         return kioscos.get(a);
     }
     
     
     
     
