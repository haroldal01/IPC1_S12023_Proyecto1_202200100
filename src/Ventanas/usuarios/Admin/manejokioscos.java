    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
       
        String ckiosko = txtcodigo.getText();
        String nom = txtnombre.getText();
        String cregion = txtregion.getSelectedItem().toString();
        
        if(!nom.isEmpty() && !ckiosko.isEmpty()){
            
            Kiosco temporal = new Kiosco(ckiosko, nom, cregion);
            
            if(insertar(temporal)){
                JOptionPane.showMessageDialog(this, "Kiosko registrado");
                txtnombre.setText("");
                txtcodigo.setText("");
                origenR.removeAllElements();
                iniciarkiosko();
                
            }else{
                JOptionPane.showMessageDialog(this, "Kiosko existente");
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Hay campos vacios");
        }
        
    }//GEN-LAST:event_btnsaveActionPerformed

