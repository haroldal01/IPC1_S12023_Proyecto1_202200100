    public void iniciarRegiones(){
        btnguardar.setEnabled(false);
        System.out.println("Oprimido");
        String[] columnas = {"Codigo","Nombre Region","Precio Estandar", "Precio Especial"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);
        int z = Logica.tama単oR();
        for(int a = 0; a<z;a++)
        {
            Object[] fila = {Logica.imprimirR(a).getNombreregion(),
                Logica.imprimirR(a).getCodigo(),
                Logica.imprimirR(a).getPrecioEstandar(),
                Logica.imprimirR(a).getPrecioEspecial()};
            modelo.addRow(fila);
        }
        tabla.setModel(modelo);
        
    }

    public String Tarjeta(){
    int row = tabla.getSelectedRow();
    int column = 0; // seleccionamos la segunda columna
    String value = "vacio";
    if(row!=-1){
       String a = tabla.getModel().getValueAt(row, column).toString();  
        if(!a.isEmpty()){
            value = a;
            
        }else{
            System.out.println("Datos vacios");
            value="vacio";  
        }
    }else{
        value = "vacio";
    }
    return value;
    }
     
