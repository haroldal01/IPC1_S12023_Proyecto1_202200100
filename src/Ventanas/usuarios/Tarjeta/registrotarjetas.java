    public void iniciarTarjeta(){
        System.out.println(correo);
        String[] columnas = {"Nombre","Numero","Fecha"};
            DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

            for(int i = 0;i<Logic.tamaño();i++)
            {
                String tarjeta = "";
                if(Logic.completa(i).getUsuario().equalsIgnoreCase(correo)){
                    int numero = Logic.completa(i).getNumerotar().length();
                    if(numero>=4)
                    {
                        
                        String ultimos4 = Logic.completa(i).getNumerotar().substring(numero-4);
                        tarjeta = "";
                        for(int a = 0; a<numero-4;a++){
                            tarjeta+="X";
                        }
                        tarjeta += ultimos4;
                        
                    }else{
                        tarjeta = Logic.completa(i).getNumerotar();
                    }
                    
                Object[] fila = 
                {Logic.completa(i).getNombretar(),tarjeta, 
                Logic.completa(i).getFechatar()};
                modelo.addRow(fila);
                }
             }
             jTable1.setModel(modelo);
    }
    
    
