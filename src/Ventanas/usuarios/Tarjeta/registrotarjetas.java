    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
        if(modificar==false)
        {
            String nom = txtnombretar.getText(); 
            String num = txtnumerotar.getText(); 
            String fec = txtfechatar.getText(); 

              if(!nom.isEmpty() && !num.isEmpty() && !fec.isEmpty()){

                Tarjeta temporal = new Tarjeta(nom,num,fec,correo); 
                 if(Logic.insertar(temporal))
                 {
                     JOptionPane.showMessageDialog(this,"Tarjeta registrada");
                     txtnombretar.setText("");
                     txtnumerotar.setText("");
                     txtfechatar.setText("");
                     iniciarTarjeta();
                 }else{
                     JOptionPane.showMessageDialog(this,"Tarjeta existente");
                 }

             }else{
                  JOptionPane.showMessageDialog(this, "Hay campos vacios");
              }
        
        }else{
            
            btnguardar.setText("ACTUALIZAR");
            
            String nom = txtnombretar.getText(); 
            String num = txtnumerotar.getText(); 
            String fec = txtfechatar.getText(); 

              if(!nom.isEmpty() && !num.isEmpty() && !fec.isEmpty()){

                    String no= Tarjeta();
                    if(!no.equalsIgnoreCase("vacio"))
                    {
                        for(int i =0;i< Logic.tamaño();i++){
                            if(Logic.completa(i).getNumerotar().equalsIgnoreCase(no))
                            {

                               if(Logic.buscar(correo, num)==-1){

                                   Logic.completa(i).setFechatar(fec);
                                   Logic.completa(i).setNombretar(nom);
                                   Logic.completa(i).setNumerotar(num);
                                   btnguardar.setText("Guardar");
                                   setModificar(false);
                                   txtnombretar.setText("");
                                   txtnumerotar.setText("");
                                   txtmodificar.setText("");
                                   txtfechatar.setText("");
                                   jTable1.setEnabled(true);
                                   iniciarTarjeta();
                                   
                               }else{
                                   JOptionPane.showMessageDialog(this, "Tarjeta existente");
                                   
                                   txtnombretar.setText("");
                                   txtnumerotar.setText("");
                                   txtfechatar.setText("");
                               }

                            }
                        }
                    }else{
                        JOptionPane.showMessageDialog(this, "Seleccione una tarjeta");
                    }
                    
             }else{
                  JOptionPane.showMessageDialog(this, "Hay campos vacios");
              }
        
        }
        
    }//GEN-LAST:event_btnguardarActionPerformed

    public String Tarjeta(){
    int row = jTable1.getSelectedRow();
    int column = 1; // seleccionamos la segunda columna
    String value = "vacio";
    if(row!=-1){
       String a = jTable1.getModel().getValueAt(row, column).toString();  
        if(!a.isEmpty()){
            value = a;
            
            if(!CVV.getText().isEmpty()){
                setCVV(CVV.getText());
            }else{
                JOptionPane.showMessageDialog(this, "Falta de CVV");
            }
            
        }else{
            System.out.println("Datos vacios");
            value="vacio";
            setCVV("vacio");
        }
    }else{
        setCVV("vacio");
        value = "vacio";
    }
    return value;
    }
    
    
