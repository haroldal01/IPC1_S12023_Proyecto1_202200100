    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEliminarActionPerformed
    {//GEN-HEADEREND:event_btnEliminarActionPerformed
           if(administrar())
           {
            CVV.setEnabled(true);
            CVV.setEditable(true);
            CVV.setText("Eliminando");
            CVV.setEditable(false);
            CVV.setEnabled(false);

            String no= Tarjeta();
            
            if(!no.equalsIgnoreCase("vacio")){
                for(int i =0;i< Logic.tamaño();i++){
                if(Logic.completa(i).getNumerotar().equalsIgnoreCase(no)){
                    System.out.println("Tarjeta eliminada "+Logic.completa(i).getNumerotar());
                    Logic.eliminar(i);
                }
            }
                iniciarTarjeta();
            }else{
                JOptionPane.showMessageDialog(this, "Seleccione una tarjeta");
            }
            
           }else{
               JOptionPane.showMessageDialog(this, "No hay tarjetas");
           }
           
    }//GEN-LAST:event_btnEliminarActionPerformed

